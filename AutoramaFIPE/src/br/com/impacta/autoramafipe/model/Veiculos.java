package br.com.impacta.autoramafipe.model;

public class Veiculos {
	private String fipe_marca;
	private String name;
	private String marca;
	private String key;
	private String id;
	private String fipe_name;

	public String getFipe_marca() {
		return fipe_marca;
	}

	public String getName() {
		return name;
	}

	public String getMarca() {
		return marca;
	}

	public String getKey() {
		return key;
	}

	public String getId() {
		return id;
	}

	public String getFipe_name() {
		return fipe_name;
	}

	public void setFipe_marca(String fipe_marca) {
		this.fipe_marca = fipe_marca;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setFipe_name(String fipe_name) {
		this.fipe_name = fipe_name;
	}
}